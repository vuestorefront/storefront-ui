<template>
  <transition name="fade">
    <SfTabs
      v-if="editAddress"
      key="edit-address"
      :open-tab="1"
      class="tab-orphan"
    >
      <SfTab title="Change the address">
        <p class="message">
          Keep your addresses and contact details updated.
        </p>
        <div class="form">
          <SfInput
            v-model="firstName"
            name="firstName"
            label="First Name"
            required
            class="form__element form__element--half"
          />
          <SfInput
            v-model="lastName"
            name="lastName"
            label="Last Name"
            required
            class="form__element form__element--half form__element--half-even"
          />
          <SfInput
            v-model="streetName"
            name="streetName"
            label="Street Name"
            required
            class="form__element"
          />
          <SfInput
            v-model="apartment"
            name="apartment"
            label="House/Apartment number"
            required
            class="form__element"
          />
          <SfInput
            v-model="city"
